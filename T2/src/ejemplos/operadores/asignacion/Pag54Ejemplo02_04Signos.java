package ejemplos.operadores.asignacion;

public class Pag54Ejemplo02_04Signos {

	public static void main(String[] args) {
		int divisionEntera = 7 / 5;
		int modulo = 7 % 2;
		double divisionReal = 7.0 / 2.0;
		System.out.println("Division Entera 7 / 2 = " + divisionEntera);
		System.out.println("Modulo 7 % 2 = " + modulo);
		System.out.println("Division Real 7.0 / 2.0  = " + divisionReal);
	}
}
