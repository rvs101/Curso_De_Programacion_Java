package conceptos.literales;

/**
 * Literales : Valores que almacenan las variables : "elementos a la izquierda
 * del igual"
 * 
 * @author RVS
 * @time 17 may 2022 - 18:53:04
 *
 */
public class LiteralesChar {

	public static void main(String[] args) {

//                        LITERALES Char
//                           ↓
		char literal1 = 'a';
		System.out.println("literal 1 " + literal1);
		char literal2 = '1';
		System.out.println("literal 2 " + literal2);
		char literal3 = '$';
		System.out.println("literal 3 " + literal3);
		char literal4 = '\u00F1';
		System.out.println("literal 4 " + literal4);
		char literal5 = '\n';
		System.out.println("literal 5 - salto de linea " + literal5);
		char literal6 = '\t';
		System.out.println("literal 2 - tabulación " + literal6);

	}
}
