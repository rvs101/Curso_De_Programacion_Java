public class Calculadora {

	/**
	 * Función de clase
	 * 
	 * @param a : int - Recibe un entero como argumento
	 * @param b : int - Recibe un entero como argumento
	 * @return la suma de los 2 parametros
	 */
	public static int suma(int a,  int b ) {
		return a + b;
	}
	
	/**
	 * Función de clase
	 * 
	 * @param a : int - Recibe un entero como argumento
	 * @param b : int - Recibe un entero como argumento
	 * @return la suma de los 2 parametros
	 */
	public static int restar(int a,  int b ) {
		return a - b;
	}
	
	
}
